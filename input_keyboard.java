import java.util.*;

//Challange Hackerrank-----> you must read 3 integers from stdin and then print them in stdout.
//--------------->Each integer must be printed on new line
public class input_keyboard {
    public static void main(String args[]) {
        // declare an scanner object that can read values from keyboard
        Scanner scan = new Scanner(System.in);

        int a = scan.nextInt();
        // read a number from keyboard
        int b = scan.nextInt();
        int c = scan.nextInt();
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

    }
}
